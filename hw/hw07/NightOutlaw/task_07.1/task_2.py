    choice = input("Площу якої фігури Ви бажаєте розрахувати?\nВведіть відповідну цифру:\n1 - прямокутник;\n2 - трикутник;\n3 - коло.\nБудь ласка, зробіть свій вибір: ")   # Запитуємо у користувача, площу якої фігури він хоче розрахувати
    if choice == '1':               # Виклик функції для обчислення площі фігури, залежно від вибору користувача
        calc_rectangle_suare()
    elif choice == '2':
        calc_triangle_square()
    elif choice == '3':
        calc_circle_square()
