import re


def validate_password(password):
    """
    Перевіряє валідність пароля за заданими критеріями.

    Критерії валідації:
    - Щонайменше 1 мала літера ([a-z]).
    - Щонайменше 1 велика літера ([A-Z]).
    - Щонайменше 1 цифра ([0-9]).
    - Щонайменше 1 спеціальний символ з [$#@].
    - Мінімальна довжина: 6 символів.
    - Максимальна довжина: 16 символів.

    Аргументи:
        password (str): Пароль для перевірки.

    Повертає:
        tuple: (bool, list) - булеве значення (True, якщо валідний; False, якщо ні)
                              та список повідомлень про помилки (порожній, якщо валідний).
    """
    errors = []

    # 1. Перевірка довжини
    if not (6 <= len(password) <= 16):
        errors.append("Довжина пароля повинна бути від 6 до 16 символів.")

    # 2. Перевірка наявності хоча б однієї малої літери
    if not re.search("[a-z]", password):
        errors.append("Пароль повинен містити хоча б одну малу літеру ([a-z]).")

    # 3. Перевірка наявності хоча б однієї великої літери
    if not re.search("[A-Z]", password):
        errors.append("Пароль повинен містити хоча б одну велику літеру ([A-Z]).")

    # 4. Перевірка наявності хоча б однієї цифри
    if not re.search("[0-9]", password):
        errors.append("Пароль повинен містити хоча б одну цифру ([0-9]).")

    # 5. Перевірка наявності хоча б одного спеціального символу
    # Символи $ та # є спеціальними в regex, тому їх потрібно екранувати (\$) (\#)
    # або помістити в символьний клас [], де вони інтерпретуються буквально.
    if not re.search("[$#@]", password):
        errors.append("Пароль повинен містити хоча б один символ з [$#@].")

    if not errors:
        return True, []
    else:
        return False, errors


if __name__ == "__main__":
    """
    Цей блок виконується, коли скрипт запускається безпосередньо.
    Він запитує пароль у користувача та виводить результат валідації.
    """
    print("Будь ласка, введіть пароль для перевірки:")
    user_password = input()

    is_valid, validation_errors = validate_password(user_password)

    if is_valid:
        print("Пароль валідний.")
    else:
        print("Пароль невалідний з наступних причин:")
        for error in validation_errors:
            print(f"- {error}")
